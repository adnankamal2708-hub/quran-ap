// ── Main ───────────────────────────────────────────────────────
var totalFixed = 0;
var totalFixedFiles = 0;

var files = fs.readdirSync(DATA_DIR);
files.sort();
files.forEach(function(file) {
  if (!/^words-.*\.js$/.test(file)) return;
  var filePath = path.join(DATA_DIR, file);
  var content = fs.readFileSync(filePath, 'utf8');
  var changed = false;
  var fileFixed = 0;

  var indices = [];
  var idx = -1;
  while ((idx = content.indexOf('oppositeWords: []', idx + 1)) >= 0) {
    indices.push(idx);
  }
  indices.reverse();
  indices.forEach(function(oppIdx) {
    var depth = 0;
    for (var i = oppIdx; i >= 0; i--) {
      if (content[i] === '}') depth++;
      else if (content[i] === '{') {
        if (depth === 0) {
          var objText = content.substring(i, oppIdx);
          var match = objText.match(/arabic:\s*'([^']*)'/);
          if (match) {
            var arabic = match[1];
            if (OPPOSITES[arabic]) {
              var replacement = "oppositeWords: ['" + OPPOSITES[arabic].join("', '") + "']";
              content = content.substring(0, oppIdx) + replacement + content.substring(oppIdx + 'oppositeWords: []'.length);
              changed = true;
              fileFixed++;
              console.log('FIXED[' + file + '] ' + arabic + ' → ' + replacement);
            }
          }
          break;
        }
        depth--;
      }
    }
  });

  if (changed) {
    fs.writeFileSync(filePath, content, 'utf8');
    totalFixed += fileFixed;
    totalFixedFiles++;
  }
});

console.log('\n' + '='.repeat(50));
console.log('Total: ' + totalFixed + ' words fixed across ' + totalFixedFiles + ' files.');
